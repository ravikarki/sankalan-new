var Modal = (function() {

  var modalOpen = document.querySelector('[data-modal="open_algo"]'),
      modalClose = document.querySelector('[data-modal="close_algo"]'),
      modalWrapper = document.querySelector('[data-modal="wrapper_algo"]');

      return {
        init: function() {
          this.abrir();
          this.fechar();
        },

        abrir: function() {
          modalOpen.onclick = function(e) {
            e.preventDefault;
              modalWrapper.classList.add("modal-opened");
          }
        },

        fechar: function() {
            modalClose.onclick = function(e) {
              e.preventDefault;
              modalWrapper.classList.remove("modal-opened");
            }
        }
      }
}());
Modal.init();
